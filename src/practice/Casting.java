package practice;

public class Casting {
	
	//implicit casting

	public static void main(String[] args) {
		int a = 12;
		double b = a;
		System.out.println(b);
		
		//explicit casting
		
		double c = 45.678;
		int d = (int) c;
		System.out.println(d);
		
		//implicit casting
		
		int f = 234567;
		long s = f;
		System.out.println(s);
		
		//explicit casting
		
		long g = 234567;
		short w = (short) g;
		System.out.println(w);
		
		//char into int
		
		char ch = 'A';
		int no = ch;
		System.out.println(no);
		
		
		


		
	}

}
